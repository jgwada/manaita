import { ShopProfile } from '@/types'
import { shopContext, sanitizeInput } from './helpers'

export const buildResearchPrompt = (shop: ShopProfile, competitorInfo: string): string => {
  const safeCompetitorInfo = sanitizeInput(competitorInfo, 500)
  return `
あなたは飲食店専門の市場調査コンサルタントです。
${shopContext(shop)}
${safeCompetitorInfo ? `\nユーザーが把握している競合情報：\n${safeCompetitorInfo}\n` : ''}

この店舗の業態・地域・客単価をもとに、競合調査レポートを作成してください。

■ 調査ルール（厳守）
- 【競合店の調査】は必ずWeb検索を使い、実在する店舗のみを記載すること。存在しない店舗を絶対に捏造しないこと。
  - 検索クエリ例：「${shop.area} ${shop.industry}」「${shop.area} 居酒屋 人気」など
  - 検索で見つからない店舗は記載しない
- 【自店の分析】（セクション3・4・5）はあなたの知識をもとに回答してよい（Web検索不要）
- 出力はコンパクトにまとめること（各セクション要点のみ）

以下の形式のみで出力してください（前置き・後置き・説明文は一切不要）：

【1. 周辺の競合店】
（Web検索で見つけた、この業態・地域で競合になる実在店を3〜5店。「店名／ジャンル／特徴（立地・規模・客単価・営業スタイル）」の形式で。検索で確認できた店のみ記載）

【2. 競合の強み】
（上記の各競合店が持つ強みを分析。価格・立地・ブランド・集客力・メニューなど観点ごとに箇条書き）

【3. 自店の勝てるポイント】
（この店のプロフィールをもとに、競合に対して差別化できる強みを5点以上。根拠も添えて具体的に）

【4. 競合に勝つための施策】
（今すぐできるもの・中期的なもの・長期的なものに分けて、具体的なアクションプランを箇条書き）

【5. 競合が使っている広告媒体・掲載先】
（この業態・地域の競合が一般的に活用している広告・集客媒体をリスト。媒体名・特徴・費用感を記載）
`
}

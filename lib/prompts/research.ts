import { ShopProfile } from '@/types'
import { shopContext } from './helpers'

export const buildResearchPrompt = (shop: ShopProfile, competitorInfo: string): string => `
あなたは飲食店専門の市場調査コンサルタントです。
${shopContext(shop)}
${competitorInfo ? `\nユーザーが把握している競合情報：\n${competitorInfo}\n` : ''}

この店舗の業態・地域・客単価をもとに、競合調査レポートを作成してください。
Claudeの学習データに含まれる一般的な飲食店市場の知識をもとに、できる限り具体的・実践的な内容にしてください。

以下の形式のみで出力してください（前置き・後置き・説明文は一切不要）：

【1. 周辺の競合店】
（この業態・地域で実際に競合になりえる店を3店、できる限り実在する店名で具体的に挙げること。知識の範囲で実名が出せる場合は「店名／ジャンル／特徴」の形式で。実名が不確かな場合は「〇〇チェーン（××店）」など可能な限り具体的に記載し、規模・席数・営業スタイルも添える）

【2. 競合の強み】
（上記の各競合タイプが持つ強みを分析。価格・立地・ブランド・集客力・メニューなど観点ごとに箇条書き）

【3. 自店の勝てるポイント】
（この店のプロフィールをもとに、競合に対して差別化できる強みを5点以上。根拠も添えて具体的に）

【4. 競合に勝つための施策】
（今すぐできるもの・中期的なもの・長期的なものに分けて、具体的なアクションプランを箇条書き）

【5. 競合が使っている広告媒体・掲載先】
（この業態・地域の競合が一般的に活用している広告・集客媒体を詳細にリスト。媒体名・URL・特徴・費用感・効果を記載）
`
